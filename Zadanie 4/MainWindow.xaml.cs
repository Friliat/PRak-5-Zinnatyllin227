        private void BtnCancel_Click(object sender, RoutedEventArgs e)
        {
            Close();
        }

        private void BtnAdd_Click(object sender, RoutedEventArgs e)
        {
            if ((String.IsNullOrEmpty(TbNumber.Text)))
            {
                return;
            }
            try
            {
                int xa = Convert.ToInt32(TbNumber.Text);
            }
            catch (FormatException)
            {
                MessageBox.Show("Введены не корректные данные");
            }
            catch (Exception ex)
            {
                MessageBox.Show(ex.Message);
            }
            ListBoxData.Items.Add(TbNumber.Text);
        }

        private void BtnOK_Click(object sender, RoutedEventArgs e)
        {
            int i = 1;
            int n = Convert.ToInt32(ListBoxData.Items[i]);
            double a = Convert.ToDouble(ListBoxData.Items[i]);
            double sum = 1;
            for (; i <= n; ++i)
            {
                sum = sum + Math.Pow(a, i);
            }
            TextBlockAnswer.Text = $"Ответ:\n{sum}";
        }
    }
